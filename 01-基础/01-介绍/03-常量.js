/**
 * 常量
 */

/*
 * JavaScript中的常量:
 * 1. 整形常量:
 *    整数, 在JavaScript中随便写一个整数, 就是整型常量;
 * 2. 实型常量:
 *    小数, 在JavaScript中随便写一个小数, 就是实型常量;
 * 3. 字符串常量:
 *    用单引号或双引号括起来的内容, 就是字符串常量; 在JavaScript中, 无论是一个字符还是多个字符, 都叫做字符串常量;
 * 4. 布尔常量:
 *    在JavaScript中, 用true表示"真", 用false表示"假";
 * 5. 自定义常量:
 *    是在ES6中新增的语法, 用const关键字自定义常量, 语法为:
 *        const 常量的名称 = 常量的取值
 */

const NAME = "ZzqGo"
console.log(NAME)
